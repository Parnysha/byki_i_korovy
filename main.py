from tkinter import *
from random import choice
#—Ñ–æ—Ä–º–∏—Ä—É–µ–º –æ–∫–Ω–æ
root = Tk()
root.geometry('300x300')
root.resizable(False,False)
root.title(f'–ë—ã–∫–∏ –∏ –∫–æ—Ä–æ–≤—ã')

#—Å–æ–∑–¥–∞—ë–º —Ñ—É–Ω–∫—Ü–∏—é –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —á–∏—Å–ª–∞ –¥–ª—è –∏–≥—Ä—ã
def generation():
    spisok=list(range(0,10)) #—Å–æ–∑–¥–∞—ë–º —Å–ø–∏—Å–æ–∫ —Ü–∏—Ñ—Ä –æ—Ç 0 –¥–æ 9
    generation_str = '' #—Å–æ–∑–¥–∞–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É
    for i in range(0,4): #–ø—Ä–æ—Ö–æ–¥–∏–º 4 —Ä–∞–∑–∞ —Ü–∏–∫–ª–æ–º
        a = choice(spisok) #–ø—Ä–∏ –ø–æ–º–æ—â–∏ —Ñ—É–Ω–∫—Ü–∏–∏ choice –∏–∑ –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ random –≤—ã–±–∏—Ä–∞–µ–º —Ä–∞–Ω–¥–æ–º–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑ —Å–ø–∏—Å–∫–∞ —Å —Ü–∏—Ñ—Ä–∞–º–∏
        spisok.remove(a) #—É–¥–∞–ª—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω–æ–µ —Ä–∞–Ω–¥–æ–º–Ω–æ–µ —á–∏—Å–ª–æ –∏–∑ —Å–ø–∏—Å–∫–∞
        generation_str +=str(a) #–∑–∞–ø–∏—Å—ã–≤–∞–µ–º —ç—Ç–æ —á–∏—Å–ª–æ –∫–∞–∫ –±—É–∫–≤—É –≤ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É
    return generation_str #–ø–æ—Å–ª–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —Ñ—É–Ω–∫—Ü–∏–∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏

number = generation()  #–≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∏ –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –µ–µ —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é number
print(number)
#—Å–æ–∑–¥–∞—ë–º –æ–±–ª–∞—Å—Ç—å –≥–ª–∞–≤–Ω–æ–≥–æ —ç–∫—Ä–∞–Ω–∞ –≤ –∫–æ—Ç–æ—Ä–æ–π –±—É–¥–µ—Ç –ø–æ–ª–µ –≤–≤–æ–¥–∞ –∏ –Ω–∞–¥–ø–∏—Å–∏ Input:, –ü–æ–ø—ã—Ç–∫–∞, –≤–≤–µ–¥—ë–Ω–Ω–æ–µ —á–∏—Å–ª–æ, —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏ –∫–Ω–æ–ø–∫–∞ –û–∫
frame_const = Frame(root, width=300, height=100)
frame_const.place(x='0', y='0')


input_word = Label(frame_const,text=f'Input:')
input_word.place(x=90,y=60,anchor='center')

popytka_word = Label(frame_const,text=f'–ü–æ–ø—ã—Ç–∫–∞')
popytka_word.place(x=60,y=90,anchor='center')

vvod_word = Label(frame_const,text=f'–í–≤–µ–¥—ë–Ω–Ω–æ–µ —á–∏—Å–ª–æ')
vvod_word.place(x=150,y=90,anchor='center')

result_word = Label(frame_const,text=f'–†–µ–∑—É–ª—å—Ç–∞—Ç')
result_word.place(x=240,y=90,anchor='center')

input_enter = Entry(frame_const)
input_enter['width']=10  #—à–∏—Ä–∏–Ω–∞ –≤–∏–¥–∏–º—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤ –≤ –ø–æ–ª–µ –≤–≤–æ–¥–∞
input_enter.place(x=150,y=60,anchor='center')


#—Å–æ–∑–¥–∞—ë–º —Ñ—Ä–µ–π–º –≤ –∫–æ—Ç–æ—Ä–æ–º –±—É–¥—É—Ç –≤—ã–≤–æ–¥–∏—Ç—Å—è –≤–≤–æ–¥–∏–º—ã–µ —á–∏—Å–ª–∞, —á–∏—Å–ª–æ –ø–æ–ø—ã—Ç–æ–∫ –∏ –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ –∏ cow
frame1 = Frame(root, width=300, height=160)
frame1.place(x='0', y='100')


result_game = Label(root,text='')


#–∑–∞–¥–∞–µ–º –Ω–∞—á–∞–ª—å–Ω—É—é –ø–æ–∑–∏—Ü–∏—é –ø–æ y –¥–ª—è –≤—ã–≤–æ–¥–∞ –ø–æ–ø—ã—Ç–æ–∫ –∏ –Ω–∞—á–∞–ª—å–Ω–æ–µ –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫
popytka = 0
position = 10
#–∑–∞–¥–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É –û–∫
def button_ok_comand():
    global position,popytka  #–¥–µ–ª–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –≥–ª–æ–±–∞–ª—å–Ω—ã–º–∏
    chislo=input_enter.get() #–±–µ—Ä–µ–º –∑–Ω–∞—á–µ–Ω–∏—è –∏–∑ –ø–æ–ª—è –≤–≤–æ–¥–∞ –∏ –∑–∞–Ω–æ—Å–∏–º –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é chislo
    input_enter.delete(0,END)  #—Å—Ç–∏—Ä–∞–µ–º —Ü–∏—Ñ—Ä—ã –∏–∑ –ø–æ–ª—è –≤–≤–æ–¥–∞ –Ω–∞ —ç–∫—Ä–∞–Ω–µ
    popytka += 1   #–ø—Ä–∏–±–∞–≤–ª—è–µ–º –ø–æ–ø—ã—Ç–∫—É
    byk=0   #–≤–≤–æ–¥–∏–º –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ–µ
    cow = 0 # –∏ –∫–æ—Ä–æ–≤
    for i in list(range(0,4)):  #–ø—Ä–æ—Ö–æ–¥–∏–º —Ü–∏–∫–ª–æ–º –ø–æ —Å–ø–∏—Å–∫—É –∏–∑ —Ü–∏—Ñ—Ä –æ—Ç 0 –¥–æ 3
        if chislo[i]==number[i]:  #–µ—Å–ª–∏ —á–∏—Å–ª–æ –∏–∑ –Ω–∞–±–æ—Ä–∞ —Ü–∏—Ñ—Ä –∏–∑ –ø–æ–ª—è –≤–≤–æ–¥–∞ –∏ –∏–∑ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö —Å–æ–≤–ø–∞–¥–∞—é—Ç –∏ –Ω–∞—Ö–æ–¥—è—Ç—Å—è –Ω–∞ –æ–¥–Ω–æ–π –ø–æ–∑–∏—Ü–∏–∏
            byk+=1 #—Ç–æ –º—ã –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –Ω–∞ 1
        elif chislo[i]!=number[i] and chislo[i] in number: #–µ—Å–ª–∏ —á–∏—Å–ª–æ –∏–∑ –Ω–∞–±–æ—Ä–∞ —Ü–∏—Ñ—Ä –∏–∑ –ø–æ–ª—è –≤–≤–æ–¥–∞ –∏ –∏–∑ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö –Ω–µ —Å–æ–≤–ø–∞–¥–∞—é—Ç, —á–∏—Å–ª–æ –∏–∑ –Ω–∞–±–æ—Ä–∞ —Ü–∏—Ñ—Ä –∏–∑ –ø–æ–ª—è –≤–≤–æ–¥–∞ –µ—Å—Ç—å –≤ —Å–ø–∏—Å–∫–µ –∏–∑ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö
            cow +=1  #—Ç–æ –º—ã –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –∫–æ—Ä–æ–≤ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º –Ω–∞ 1
    result = f'üêÇ{byk}üêÑ{cow}' #–≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é result –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ –∏ –∫–æ—Ä–æ–≤
    #–≤—ã–≤–æ–¥–∏–º –ø–æ–ø—ã—Ç–∫–∏, –≤–≤–µ–¥–µ–Ω–Ω–æ–µ —á–∏—Å–ª–æ, –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ –∏ –∫–æ—Ä–æ–≤
    pop_word = Label(frame1, text=f'{popytka}')
    pop_word.place(x=50, y=position,anchor='center')
    chi_word = Label(frame1, text=f'{chislo}')
    chi_word.place(x=140, y=position,anchor='center')
    res_word = Label(frame1, text=f'{result}')
    res_word.place(x=220, y=position,anchor='center')
    position +=30 #–ø—Ä–∏–±–∞–≤–ª—è–µ–º –∫ –ø–æ–∑–∏—Ü–∏–∏ 30
    if popytka == 6: #–µ—Å–ª–∏ –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ø—ã—Ç–æ–∫ —Ä–∞–≤–Ω–æ 6, —Ç–æ –≤—ã–≤–æ–¥–∏—Ç—Å—è —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –ø—Ä–æ–∏–≥—Ä—ã—à–µ –∏ —Å–∫—Ä—ã–≤–∞—é—Ç—Å—è –¥–≤–∞ —Ñ—Ä–µ–π–º–∞
        result_game['text'] = '–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏'
        result_game.place(relx=0.5,rely=0.3,anchor='center')
        frame1.place_forget()
        frame_const.place_forget()
    elif byk == 4:  #–µ—Å–ª–∏ –∫–æ–ª–ª–∏—á–µ—Å—Ç–≤–æ –±—ã–∫–æ–≤ –±—É–¥–µ—Ç —Ä–∞–≤–Ω–æ 4, —Ç–æ –≤—ã–≤–æ–¥–∏—Ç—Å—è —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –ø–æ–±–µ–¥–µ –∏ —Å–∫—Ä—ã–≤–∞—é—Ç—Å—è –¥–≤–∞ —Ñ—Ä–µ–π–º–∞
        result_game['text'] = '–í—ã –ø–æ–±–µ–¥–∏–ª–∏'
        result_game.place(relx=0.5, rely=0.3, anchor='center')
        frame1.destroy()
        frame_const.place_forget()
#—Ñ—É–Ω–∫—Ü–∏—è –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –Ω–∞ –∫–Ω–æ–ø–∫—É –Ω–∞—á–∞—Ç—å –∏–≥—Ä—É
def button_new_comand():
    global number,popytka,position,frame1
    #–≥–µ–Ω–µ—Ä–∏—Ä—É–µ–º –Ω–æ–≤–æ–µ —á–∏—Å–ª–æ, —Å—Ç–∞–≤–∏–º —á–∏—Å–ª–æ –ø–æ–ø—ã—Ç–æ–∫ 0 –∏ –ø–æ–∑–∏—Ü–∏—é 10
    number = generation()
    popytka = 0
    position = 10
    frame_const.place(x='0', y='0') #–ø–æ–∫–∞–∂–µ–º –Ω–∞ —ç–∫—Ä–∞–Ω–µ –æ–±–ª–∞—Å—Ç—å —Å input, –∫–Ω–æ–ø–∫–æ–π –û–∫ –∏ —Ç.–¥.
    frame1 = Frame(root, width=300, height=160) #–∞ —Ñ—Ä–µ–π–º –≥–¥–µ –±—ã–ª–∏ –ø–æ–ø—ã—Ç–∫–∏ –ø–µ—Ä–µ–∑–∞–ø–∏—à–µ–º, —á—Ç–æ–±—ã —Å—Ç–µ—Ä–µ—Ç—å –≤—Å–µ —á—Ç–æ –≤ –Ω–µ–º –±—ã–ª–æ
    frame1.place(x='0', y='100')
    result_game.place_forget()  #—Å–∫—Ä–æ–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –∏–≥—Ä—ã
def button_exit_comand():
    exit()
#–∑–∞–ø–∏—Å—ã–≤–∞–µ–º —á—Ç–æ –ø—Ä–∏ –Ω–∞–∂–∞—Ç–∏–∏ –ª–µ–≤–æ–π –∫–Ω–æ–ø–∫–æ–π –º—ã—à–∏ –ø–æ —Ç–∞–∫–æ–π —Ç–æ –∫–Ω–æ–ø–∫–µ, –±—É–¥–µ—Ç –≤—ã–∑—ã–≤–∞—Ç—å —Ç–∞–∫—É—é —Ç–æ —Ñ—É–Ω–∫—Ü–∏—é
button_ok = Button(frame_const,text='Ok',command=button_ok_comand)
